def score_ideas(yt_output, research_output):
    """Cross-reference and score ideas."""
    scored_ideas = []
    
    # Combine and score
    for idea in yt_output["ideas"]:
        score = idea["confidence_score"]
        scored_ideas.append({
            "title": idea["title"],
            "hook": idea["hook"],
            "score": score,
            "sources": idea["source_signals"],
            "views": idea["estimated_views"],
            "complexity": idea["production_complexity"]
        })
    
    for trend in research_output["trends"]:
        score = trend["confidence_score"]
        scored_ideas.append({
            "title": f"The Truth About {trend['topic']}",
            "hook": trend["content_angle"],
            "score": score,
            "sources": trend["sources"],
            "views": "TBD",
            "complexity": "medium",
            "urgency": trend["urgency"]
        })
    
    # Sort by score
    scored_ideas.sort(key=lambda x: x["score"], reverse=True)
    return scored_ideas

def generate_brief(scored_ideas, output_path):
    """Generate Idea Mining Brief."""
    date_str = datetime.now().strftime("%Y-%m-%d")
    
    brief = f"""# Idea Mining Brief — {date_str}

## Top 5 Video Ideas

"""
    
    for i, idea in enumerate(scored_ideas[:5], 1):
        brief += f"""### {i}. {idea['title']} — Score: {idea['score']}/10
**Hook:** {idea['hook']}
**Sources:** {', '.join(idea['sources'])}
**Estimated Views:** {idea['views']}
**Production Complexity:** {idea['complexity']}

"""
    
    brief += f"""## Batch Production Plan
- **Priority 1 (9.0+):** Immediate production
- **Priority 2 (8.0-8.9):** This week's queue
- **Priority 3 (7.0-7.9):** Next week's pipeline

## Content Calendar Suggestion
- **Monday:** {scored_ideas[0]['title'] if scored_ideas else 'TBD'}
- **Wednesday:** {scored_ideas[1]['title'] if len(scored_ideas) > 1 else 'TBD'}
- **Friday:** {scored_ideas[2]['title'] if len(scored_ideas) > 2 else 'TBD'}

---
*Generated by Idea Mining System*  
*Next scan: Tomorrow 09:00*
"""
    
    # Save brief
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    with open(output_path, 'w') as f:
        f.write(brief)
    
    print(f"Idea Mining Brief saved to: {output_path}")
    return brief

if __name__ == '__main__':
    parser = argparse.ArgumentParser(description='Run Idea Mining for Neuro-Finance')
